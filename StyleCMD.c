#include <stdio.h>

void ast(int type){

    // statics
    if (type==1){
        printf("||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||\n");
    }
    if (type==2){
        printf("////////////////////////////////////////////////////////////////////////////////\n");
    }
    if (type==3){
        printf("--------------------------------------------------------------------------------\n");
    }
    if (type==4){
        printf("________________________________________________________________________________\n");
    }
    
    if (type==5){
        printf("################################################################################\n");
    }
    if (type==6){
        printf("++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++\n");
    }
    
    if (type==7){
        printf("********************************************************************************\n");
    }
    if (type==8){
        printf("<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<\n");
    }
    if (type==9){
        printf(">>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>\n");
    }
    if (type==10){
        printf("""""""""""""""""""""""""""""""""""""""""""""""""""""""""""""""""""""""""""""""""\n");
    }
}

